//		描画処理
//
//====================================================================
void DwarfBase::Draw() 
{
	/* 以下はテクスチャが来たら使用 */
	SHADER->SetWorld(
		DirectX::XMMatrixScaling(
			m_size.x, m_size.y, m_size.z)
		* DirectX::XMMatrixTranslation(
			m_pos.x, m_pos.y, m_pos.z));

	m_pCube->Draw();	
}


